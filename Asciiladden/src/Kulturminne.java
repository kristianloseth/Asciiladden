public class Kulturminne {
    private String id = "";
    private String navn = ""; 
    private String beskrivelse = "";
    private String datering = "";
    
    public String getId() {
        return id;        
    }
    
    public void setId(String i) {
        id = i;
    } 
    
    public String getNavn() {
        return navn;
    }
    
    public void setNavn(String n) {
        navn = n;        
    }
    
    public String getBeskrivelse() {
        return beskrivelse;
    }
    
    public void setBeskrivelse(String b) {
        beskrivelse = b;
    }
    
    public String getDatering() {
        return datering;
    }
    
    public void setDatering(String d) {
        datering = d;
    }
    
}
